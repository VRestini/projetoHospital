public class Consultorio {
    private int codConsul;
    private int numConsu;
    private String especializacaoConsul;
    private int localConsul;

    public int getCodConsul() {
        return codConsul;
    }

    public void setCodConsul(int codConsul) {
        this.codConsul = codConsul;
    }

    public int getNumConsu() {
        return numConsu;
    }

    public void setNumConsu(int numConsu) {
        this.numConsu = numConsu;
    }

    public String getEspecializacaoConsul() {
        return especializacaoConsul;
    }

    public void setEspecializacaoConsul(String especializacaoConsul) {
        this.especializacaoConsul = especializacaoConsul;
    }

    public int getLocalConsul() {
        return localConsul;
    }

    public void setLocalConsul(int localConsul) {
        this.localConsul = localConsul;
    }
}
