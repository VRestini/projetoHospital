public class Paciente {
    private int cdPaci;
    private int telefonePaci;
    private String sexoPaci;
    private String nomePaci;
    private String historicoPaci;
    private String religiaoPaci;
    private int idadePaci;

    public int getCdPaci() {
        return cdPaci;
    }

    public void setCdPaci(int cdPaci) {
        this.cdPaci = cdPaci;
    }

    public int getTelefonePaci() {
        return telefonePaci;
    }

    public void setTelefonePaci(int telefonePaci) {
        this.telefonePaci = telefonePaci;
    }

    public String getSexoPaci() {
        return sexoPaci;
    }

    public void setSexoPaci(String sexoPaci) {
        this.sexoPaci = sexoPaci;
    }

    public String getNomePaci() {
        return nomePaci;
    }

    public void setNomePaci(String nomePaci) {
        this.nomePaci = nomePaci;
    }

    public String getHistoricoPaci() {
        return historicoPaci;
    }

    public void setHistoricoPaci(String historicoPaci) {
        this.historicoPaci = historicoPaci;
    }

    public String getReligiaoPaci() {
        return religiaoPaci;
    }

    public void setReligiaoPaci(String religiaoPaci) {
        this.religiaoPaci = religiaoPaci;
    }

    public int getIdadePaci() {
        return idadePaci;
    }

    public void setIdadePaci(int idadePaci) {
        this.idadePaci = idadePaci;
    }
}
