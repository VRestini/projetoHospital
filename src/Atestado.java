public class Atestado {
    private int cdAtestado;
    private String resultadosConsultaAtestado;
    private String medicamentoAtestado;
    private int consAtestado;

    public int getCdAtestado() {
        return cdAtestado;
    }

    public void setCdAtestado(int cdAtestado) {
        this.cdAtestado = cdAtestado;
    }

    public String getResultadosConsultaAtestado() {
        return resultadosConsultaAtestado;
    }

    public void setResultadosConsultaAtestado(String resultadosConsultaAtestado) {
        this.resultadosConsultaAtestado = resultadosConsultaAtestado;
    }

    public String getMedicamentoAtestado() {
        return medicamentoAtestado;
    }

    public void setMedicamentoAtestado(String medicamentoAtestado) {
        this.medicamentoAtestado = medicamentoAtestado;
    }

    public int getConsAtestado() {
        return consAtestado;
    }

    public void setConsAtestado(int consAtestado) {
        this.consAtestado = consAtestado;
    }
}
