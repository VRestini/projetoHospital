public class Exame {
    private int cdExame;
    private TipoExame tipoExa;
    private String resultExame;
    private int localExame;
    private int medicoExame;

    public int getCdExame() {
        return cdExame;
    }

    public void setCdExame(int cdExame) {
        this.cdExame = cdExame;
    }

    public TipoExame getTipoExa() {
        return tipoExa;
    }

    public void setTipoExa(TipoExame tipoExa) {
        this.tipoExa = tipoExa;
    }

    public String getResultExame() {
        return resultExame;
    }

    public void setResultExame(String resultExame) {
        this.resultExame = resultExame;
    }

    public int getLocalExame() {
        return localExame;
    }

    public void setLocalExame(int localExame) {
        this.localExame = localExame;
    }

    public int getMedicoExame() {
        return medicoExame;
    }

    public void setMedicoExame(int medicoExame) {
        this.medicoExame = medicoExame;
    }
}
