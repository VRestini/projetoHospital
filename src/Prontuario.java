public class Prontuario {
    private int cdProntua;
    private String resConsExam;
    private int historicoPaci;

    public int getCdProntua() {
        return cdProntua;
    }

    public void setCdProntua(int cdProntua) {
        this.cdProntua = cdProntua;
    }

    public String getResConsExam() {
        return resConsExam;
    }

    public void setResConsExam(String resConsExam) {
        this.resConsExam = resConsExam;
    }

    public int getHistoricoPaci() {
        return historicoPaci;
    }

    public void setHistoricoPaci(int historicoPaci) {
        this.historicoPaci = historicoPaci;
    }
}
