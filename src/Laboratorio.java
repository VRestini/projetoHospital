public class Laboratorio {
    private int cdLab;
    private String numLab;
    private EspecializacaoLab especializacaoLab;
    private int localLab;

    public int getCdLab() {
        return cdLab;
    }

    public void setCdLab(int cdLab) {
        this.cdLab = cdLab;
    }

    public String getNumLab() {
        return numLab;
    }

    public void setNumLab(String numLab) {
        this.numLab = numLab;
    }

    public EspecializacaoLab getEspecializacaoLab() {
        return especializacaoLab;
    }

    public void setEspecializacaoLab(EspecializacaoLab especializacaoLab) {
        this.especializacaoLab = especializacaoLab;
    }

    public int getLocalLab() {
        return localLab;
    }

    public void setLocalLab(int localLab) {
        this.localLab = localLab;
    }
}
