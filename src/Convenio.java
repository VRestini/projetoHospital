public class Convenio {
    private int cdConv;
    private String tipoConv;
    private String nomeConv;

    public int getCdConv() {
        return cdConv;
    }

    public void setCdConv(int cdConv) {
        this.cdConv = cdConv;
    }

    public String getTipoConv() {
        return tipoConv;
    }

    public void setTipoConv(String tipoConv) {
        this.tipoConv = tipoConv;
    }

    public String getNomeConv() {
        return nomeConv;
    }

    public void setNomeConv(String nomeConv) {
        this.nomeConv = nomeConv;
    }
}
